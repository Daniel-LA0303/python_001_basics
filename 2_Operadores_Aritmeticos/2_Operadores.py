"""
operadores aritmeticos
    +
    -
    *
    /
    %
    **
    // division entera
"""
#Suma
print(2+3)
#Resta
print(12 - 4)
#Multiplicacion
print(3*4)
#Division
print(20/5)

#Modulo
print (2**3)

#esto es
print(20//6)


"""
Operador de pertenencia
    in
    not in
"""
#Ejemplo de in
cadena = "Hola Mundo"
print("H" in cadena)
print("h" in cadena)

#Ejemplo de not in
print("H" not in cadena)
print("h" not in cadena)

"""
operador de identidad
    is
    is not
"""
#Ejemplo de is
a = 10
b = 10
print(a is b)

#Ejemplo de is not
print(a is not b)



